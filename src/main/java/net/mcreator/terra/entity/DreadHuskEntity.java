	private final ServerBossEvent bossInfo = new ServerBossEvent(this.getDisplayName(), ServerBossEvent.BossBarColor.RED,
			ServerBossEvent.BossBarOverlay.PROGRESS);

	public DreadHuskEntity(FMLPlayMessages.SpawnEntity packet, Level world) {
		this(TerraModEntities.DREAD_HUSK, world);
	}

	public DreadHuskEntity(EntityType<DreadHuskEntity> type, Level world) {
		super(type, world);
		xpReward = 10000;
		setNoAi(false);
		setPersistenceRequired();
		this.setPathfindingMalus(BlockPathTypes.WATER, 0);
		this.moveControl = new MoveControl(this) {
			@Override
			public void tick() {
				if (DreadHuskEntity.this.isInWater())
					DreadHuskEntity.this.setDeltaMovement(DreadHuskEntity.this.getDeltaMovement().add(0, 0.005, 0));
				if (this.operation == MoveControl.Operation.MOVE_TO && !DreadHuskEntity.this.getNavigation().isDone()) {
					double dx = this.wantedX - DreadHuskEntity.this.getX();
					double dy = this.wantedY - DreadHuskEntity.this.getY();
					double dz = this.wantedZ - DreadHuskEntity.this.getZ();
					float f = (float) (Mth.atan2(dz, dx) * (double) (180 / Math.PI)) - 90;
					float f1 = (float) (this.speedModifier * DreadHuskEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
					DreadHuskEntity.this.setYRot(this.rotlerp(DreadHuskEntity.this.getYRot(), f, 10));
					DreadHuskEntity.this.yBodyRot = DreadHuskEntity.this.getYRot();
					DreadHuskEntity.this.yHeadRot = DreadHuskEntity.this.getYRot();
					if (DreadHuskEntity.this.isInWater()) {
						DreadHuskEntity.this.setSpeed((float) DreadHuskEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
						float f2 = -(float) (Mth.atan2(dy, (float) Math.sqrt(dx * dx + dz * dz)) * (180 / Math.PI));
						f2 = Mth.clamp(Mth.wrapDegrees(f2), -85, 85);
						DreadHuskEntity.this.setXRot(this.rotlerp(DreadHuskEntity.this.getXRot(), f2, 5));
						float f3 = Mth.cos(DreadHuskEntity.this.getXRot() * (float) (Math.PI / 180.0));
						DreadHuskEntity.this.setZza(f3 * f1);
						DreadHuskEntity.this.setYya((float) (f1 * dy));
					} else {
						DreadHuskEntity.this.setSpeed(f1 * 0.05F);
					}
				} else {
					DreadHuskEntity.this.setSpeed(0);
					DreadHuskEntity.this.setYya(0);
					DreadHuskEntity.this.setZza(0);
				}
			}
		};
	}

	@Override
	public Packet<?> getAddEntityPacket() {
		return NetworkHooks.getEntitySpawningPacket(this);
	}

	@Override
	protected PathNavigation createNavigation(Level world) {
		return new WaterBoundPathNavigation(this, world);
	}

	@Override
	protected void registerGoals() {
		super.registerGoals();
		this.targetSelector.addGoal(1, new NearestAttackableTargetGoal(this, Player.class, true, false));
		this.goalSelector.addGoal(2, new MeleeAttackGoal(this, 2, false));
		this.goalSelector.addGoal(3, new RandomStrollGoal(this, 1));
		this.targetSelector.addGoal(4, new HurtByTargetGoal(this));
		this.goalSelector.addGoal(5, new RandomLookAroundGoal(this));
		this.goalSelector.addGoal(6, new FloatGoal(this));
	}

